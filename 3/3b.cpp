// greater of two number
#include<iostream>
using namespace std;
int main()
{
    int a;
    cout<<"Enter the value of A"<<endl;
    cin>>a;
    int b;
    cout<<"Enter the value of B"<<endl;
    cin>>b;
    if(a>b)
    {
        cout<<"A is greater"<<endl;
    }
    if(b>a)
    {
        cout<<"B is greater"<<endl;
    }
    return 0;
}